export function mergeAuthTranslations<T extends Record<SupportedLanguage, Record<string, unknown>>>(
  translations: T
): T {
  const languages = Object.keys(translations) as SupportedLanguage[]

  for (const language of languages) {
    const localized = localizedSections[language] ?? authTranslationSections
    translations[language] = {
      ...translations[language],
      auth: localized.auth,
      routes: localized.routes
    }
  }

  return translations
}
